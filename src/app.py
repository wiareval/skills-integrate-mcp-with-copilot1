@app.get("/")
def root():
    return RedirectResponse(url="/static/index.html")


@app.post("/auth/login", response_model=LoginResponse)
def login(login_request: LoginRequest):
    """Authenticate teacher and return access token"""
    teachers_data = load_teachers()
    
    if (login_request.username in teachers_data["teachers"] and 
        teachers_data["teachers"][login_request.username]["password"] == login_request.password):
        
        access_token_expires = timedelta(minutes=ACCESS_TOKEN_EXPIRE_MINUTES)
        access_token = create_access_token(
            data={"sub": login_request.username}, expires_delta=access_token_expires
        )
        
        user_info = {
            "username": login_request.username,
            "name": teachers_data["teachers"][login_request.username]["name"]
        }
        
        return {
            "access_token": access_token,
            "token_type": "bearer",
            "user_info": user_info
        }
    else:
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail="Incorrect username or password",
            headers={"WWW-Authenticate": "Bearer"},
        )


@app.get("/auth/verify")
def verify_auth(current_user: str = Depends(get_current_user)):
    """Verify if user is authenticated"""
    teachers_data = load_teachers()
    return {
        "authenticated": True,
        "user_info": {
            "username": current_user,
            "name": teachers_data["teachers"][current_user]["name"]
        }
    }


@app.get("/activities")
def get_activities():
    return activities


@app.post("/activities/{activity_name}/signup")
def signup_for_activity(activity_name: str, email: str, current_user: str = Depends(get_current_user)):
    """Sign up a student for an activity (teachers only)"""
    # Validate activity exists
    if activity_name not in activities:
        raise HTTPException(status_code=404, detail="Activity not found")

    # Get the specific activity
    activity = activities[activity_name]

    # Validate student is not already signed up
    if email in activity["participants"]:
        raise HTTPException(
            status_code=400,
            detail="Student is already signed up"
        )

    # Add student
    activity["participants"].append(email)
    return {"message": f"Signed up {email} for {activity_name}"}


@app.delete("/activities/{activity_name}/unregister")
def unregister_from_activity(activity_name: str, email: str, current_user: str = Depends(get_current_user)):
    """Unregister a student from an activity (teachers only)"""
    # Validate activity exists
    if activity_name not in activities:
        raise HTTPException(status_code=404, detail="Activity not found")

    # Get the specific activity
    activity = activities[activity_name]

    # Validate student is signed up
    if email not in activity["participants"]:
        raise HTTPException(
            status_code=400,
            detail="Student is not signed up for this activity"
        )

    # Remove student
    activity["participants"].remove(email)
    return {"message": f"Unregistered {email} from {activity_name}"}
